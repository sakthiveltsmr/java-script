// function declaration
function hai() {
  let a = 10;
  let b = 20;
  c = a + b;
  console.log(c);
}
hai();

//function expression(named function)
const hello = function hello() {
  let a = 10;
  let b = 50;
  c = a + b;
  console.log(c);
};
hello();

//anonymous function expression
const arun = () => {
  let a = 10;
  let b = 550;
  c = a + b;
  console.log(c);
};
arun();
